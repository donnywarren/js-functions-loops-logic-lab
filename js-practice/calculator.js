function add(num1, num2) {
  let total = num1 + num2;
  alert(`${num1} + ${num2} = ${total}`);
}

function subtract(num1, num2) {
  let total = num1 - num2;
  alert(`${num1} - ${num2} = ${total}`);
}

function multiply(num1, num2) {
  let total = num1 * num2;
  alert(`${num1} * ${num2} = ${total}`);
}

function divide(num1, num2) {
  let total = num1 / num2;
  alert(`${num1} / ${num2} = ${total}`);
}

console.log(divide(4, 5));